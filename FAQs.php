    <p id="hey3"><center><b><h2>Why Do You Need Health Insurance?</h2></b></center></p><p>
        There is more than one reason to get insured! Apart from softening the financial impact of illnesses and accidents, health insurance, nowadays, even covers your everyday medical expenses. You may think getting a health insurance policy is unnecessary if you have a group health cover. But if you solely rely on your group health policy, your health will lack protection when you switch jobs and when you retire.<br>
        Health insurance premiums rise with age and also as you develop diseases, so it is advisable to buy a health cover while you are young and healthy.</p><br>
        <p><h3>Increase in the incidence of lifestyle-related illnesses</h3> <p>Life expectancy has increased. Thanks to advancement in medicines, the average man is likely to live for around 84 years by 2040. But a sedentary lifestyle, unhealthy eating habits and high stress levels are the growing norm too.

This has also given rise to the early onset of chronic diseases like cancer, lung conditions and stroke, claiming younger lives. Health insurance mitigates the financial risk that may befall a person who is diagnosed with a lifestyle disease.</p><br>

<h3>Increase in out-of-pocket expenses</h3><p>
With the healthcare industry in India witnessing double-digit inflation, it is getting extremely expensive to treat ailments in India. As a result, this puts a dent in an individual’s pocket.

Buying health insurance can double up as your emergency financial fund while preparing you for medical contingencies.</p><br>

<h3>Your group health cover may not be sufficient</h3><p>
A group health plan may or may not cover all your family members. The sum insured limit in a group plan may also not be sufficient to meet all the medical expenses incurred. Also, during the times when you are in the process of shifting to another job or are unemployed, you and your family will be exposed to the risk of unexpected medical expenses.

And then as you’ll grow older, you might need frequent medical attention. Before that you may not want to put yourself at the risk of being uninsured.

According to a survey report, nearly 95% of Indians are under-insured, with those above 45 in the highest risk group.</p><br>

<h3>Health insurance coverage is more than just hospitalization</h3><p>
Many health insurance plans nowadays give coverage for day care procedures and OPD, other than treatments involving hospitalisation. Health insurance policies also cover pre and post-hospitalisation as a large amount of expense on doctor consultations, tests and medicines are borne during this period. There are also health plans that cover vector-borne diseases like dengue. Domiciliary treatments, Ayurveda, Unani, Siddha and Homeopathy are also covered by the best health insurance plans in India.</p><br>
